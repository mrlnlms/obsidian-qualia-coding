import esbuild from "esbuild";
import process from "process";
import { builtinModules } from "node:module";
import { readFile } from "node:fs/promises";
import { gzipSync } from "fflate";

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = process.argv[2] === "production";

// Inline DuckDB-Wasm worker source as a string literal.
// Obsidian Community Plugins distribute exactly main.js + manifest.json + styles.css —
// no separate worker file is permitted, so the worker must live inside main.js
// and be turned into a Blob URL at runtime (see src/csv/duckdb/duckdbBootstrap.ts).
const duckdbWorkerInlinePlugin = {
	name: "duckdb-worker-inline",
	setup(build) {
		build.onLoad({ filter: /duckdb-browser-eh\.worker\.js$/ }, async (args) => {
			const source = await readFile(args.path, "utf8");
			return {
				contents: `export default ${JSON.stringify(source)};`,
				loader: "js",
			};
		});
	},
};

// Compress the DuckDB WASM bytes with gzip at build time. The .wasm is ~34MB
// raw and compresses to ~9-10MB (WASM has lots of redundancy). Without this
// the embedded bundle ships at 49MB which trips Community Plugins guidance.
// Runtime decompress in `wasmAssets.ts` via fflate (already a dep) costs
// ~10-30ms on first DuckDB boot — acceptable for a feature that's lazy-init.
const duckdbWasmGzipPlugin = {
	name: "duckdb-wasm-gzip",
	setup(build) {
		build.onLoad({ filter: /duckdb-eh\.wasm$/ }, async (args) => {
			const raw = await readFile(args.path);
			const gz = gzipSync(raw, { level: 9 });
			console.log(`[duckdb-wasm-gzip] ${args.path}: ${(raw.length / 1024 / 1024).toFixed(1)}MB → ${(gz.length / 1024 / 1024).toFixed(1)}MB gz`);
			return {
				contents: gz,
				loader: "binary",
			};
		});
	},
};

const context = await esbuild.context({
	banner: { js: banner },
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtinModules,
	],
	format: "cjs",
	target: "es2020",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
	minify: prod,
	loader: {
		// DuckDB-Wasm bytes embedded as Uint8Array literal in the bundle.
		".wasm": "binary",
	},
	plugins: [duckdbWorkerInlinePlugin, duckdbWasmGzipPlugin],
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
